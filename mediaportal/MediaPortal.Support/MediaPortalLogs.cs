using System.IO;

namespace MediaPortal.Support
{
  public class MediaPortalLogs : ILogCreator
  {
    private string mplogdir;

    public MediaPortalLogs(string mplogdir)
    {
      this.mplogdir = mplogdir;
    }

    public void CreateLogs(string destinationFolder)
    {
      destinationFolder = Path.GetFullPath(destinationFolder);

      string[] logFiles = Directory.GetFiles(mplogdir, "*.log");
      foreach (string logFile in logFiles)
      {
        File.Copy(logFile, Path.Combine(destinationFolder, Path.GetFileName(logFile)), true);
      }

      string[] bakFiles = Directory.GetFiles(mplogdir, "*.bak");
      foreach (string bakFile in bakFiles)
      {
        File.Copy(bakFile, Path.Combine(destinationFolder, Path.GetFileName(bakFile)), true);
      }
    }

    public string ActionMessage
    {
      get { return "Gathering log files generated by MediaPortal..."; }
    }
  }
}