        case 0xEA: w = (WORD)0x0152; break;
        case 0xEB: w = (WORD)0xBA;   break;
        case 0xEC: w = (WORD)0xDE;   break;
        case 0xED: w = (WORD)0x0166; break;
        case 0xEE: w = (WORD)0x014A; break;
        case 0xEF: w = (WORD)0x0149; break;
