using System;
using System.Collections.Generic;
using Gentle.Framework;
using TvLibrary.Log;

namespace TvDatabase
{
  /// <summary>
  /// Instances of this class represent the properties and methods of a row in the table <b>CardGroupMap</b>.
  /// </summary>
  [TableName("CardGroupMap")]
  public class CardGroupMap : Persistent
  {
    #region Members

    private bool isChanged;
    [TableColumn("idMapping", NotNull = true), PrimaryKey(AutoGenerated = true)] private int idMapping;
    [TableColumn("idCard", NotNull = true)] private int idCard;
    [TableColumn("idCardGroup", NotNull = true)] private int idCardGroup;

    #endregion

    #region Constructors

    /// <summary> 
    /// Create a new object by specifying all fields (except the auto-generated primary key field). 
    /// </summary> 
    public CardGroupMap(int idCard, int idCardGroup)
    {
      isChanged = true;
      this.idCard = idCard;
      this.idCardGroup = idCardGroup;
    }

    /// <summary> 
    /// Create an object from an existing row of data. This will be used by Gentle to 
    /// construct objects from retrieved rows. 
    /// </summary> 
    public CardGroupMap(int idMapping, int idCard, int idCardGroup)
    {
      this.idMapping = idMapping;
      this.idCard = idCard;
      this.idCardGroup = idCardGroup;
    }

    #endregion

    #region Public Properties

    /// <summary>
    /// Indicates whether the entity is changed and requires saving or not.
    /// </summary>
    public bool IsChanged
    {
      get { return isChanged; }
    }

    /// <summary>
    /// Property relating to database column idMapping
    /// </summary>
    public int IdMapping
    {
      get { return idMapping; }
    }

    /// <summary>
    /// Property relating to database column idCard
    /// </summary>
    public int IdCard
    {
      get { return idCard; }
      set
      {
        isChanged |= IdCard != value;
        IdCard = value;
      }
    }

    /// <summary>
    /// Property relating to database column idCardGroup
    /// </summary>
    public int IdCardGroup
    {
      get { return idCardGroup; }
      set
      {
        isChanged |= idCardGroup != value;
        idCardGroup = value;
      }
    }

    #endregion

    #region Storage and Retrieval

    /// <summary>
    /// Static method to retrieve all instances that are stored in the database in one call
    /// </summary>
    public static IList<CardGroupMap> ListAll()
    {
      return Broker.RetrieveList<CardGroupMap>();
    }

    /// <summary>
    /// Retrieves an entity given it's id.
    /// </summary>
    public static CardGroupMap Retrieve(int id)
    {
      // Return null if id is smaller than seed and/or increment for autokey
      if (id < 1)
      {
        return null;
      }
      Key key = new Key(typeof (CardGroupMap), true, "idMapping", id);
      return Broker.RetrieveInstance<CardGroupMap>(key);
    }

    /// <summary>
    /// Retrieves an entity given it's id, using Gentle.Framework.Key class.
    /// This allows retrieval based on multi-column keys.
    /// </summary>
    public static CardGroupMap Retrieve(Key key)
    {
      return Broker.RetrieveInstance<CardGroupMap>(key);
    }

    /// <summary>
    /// Persists the entity if it was never persisted or was changed.
    /// </summary>
    public override void Persist()
    {
      if (IsChanged || !IsPersisted)
      {
        try
        {
          base.Persist();
        }
        catch (Exception ex)
        {
          Log.Error("Exception in CardGroupMap.Persist() with Message {0}", ex.Message);
          return;
        }
        isChanged = false;
      }
    }

    #endregion

    #region Relations

    public Card ReferringCard()
    {
      return Card.Retrieve(idCard);
    }

    public CardGroup ReferringCardGroup()
    {
      return CardGroup.Retrieve(idCardGroup);
    }

    #endregion
  }
}